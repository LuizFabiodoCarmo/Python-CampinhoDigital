''' tipos de dados numéricos'''
print("Python tem três tipos: int, float, and complex")

# criar váriavel: int. 
myValue = 1 

# mostra o valor da váriavel declarada.
print(myValue)

#saber o tipo de dado armazenado na váriavel myValue.
print(type(myValue))

#combina números e texto e permite usar String.
print(str(myValue) + " é do tipo de dado: " + str(type(myValue)))

# criar váriavel: float 
myValue = 3.14 

# mostra o valor da váriavel declarada.
print(myValue)

#saber o tipo de dado armazenado na váriavel myValue.
print(type(myValue))

#combina números e texto e permite usar String.
print(str(myValue) + " é do tipo de dado: " + str(type(myValue)))

# criar váriavel: complex 
myValue = 5j

# mostra o valor da váriavel declarada.
print(myValue)

#saber o tipo de dado armazenado na váriavel myValue.
print(type(myValue))

#combina números e texto e permite usar String.
print(str(myValue) + " é do tipo de dado: " + str(type(myValue)))